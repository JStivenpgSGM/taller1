import java.lang.System;
public class Numeros {
    public static void main(String[] args) {
        int a = 201_123;
        byte b = (byte)a;
        System.out.println(b);

        float a1 = 3.5f;
        float b1 = (float)3.5;
        System.out.println(b1);

        char myUnicodeSymbol = '\uD834';
        System.out.println(myUnicodeSymbol);

    }
}
